course = "  Python programming"
# for upper case
print(course.upper())

# for lower case
print(course.lower())

# for strip the blank spaces
print(course.strip())

# for title formate
print(course.title())

# for finding index
print(course.find("Pro"))
print(course.find("pro"))

# for repleacing
print(course.replace("p", "j"))

# for checking boolean
print("pro" in course)
